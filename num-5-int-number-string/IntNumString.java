// What is the difference between int, Integer, and String?

// Explanation:
// int is a primitive data type that is used to store integer values.
// Integer is a reference data type that is used to store integer values.
// String is a reference data type that is used to store string values. such as words, sentences. E.t.c.
public class IntNumString {
    public static void main(String[] args) {
        int i = 5;
        Integer j = 5;
        String s = "5";

        System.out.println(i);
        System.out.println(j);
        System.out.println(s);
    }
}

